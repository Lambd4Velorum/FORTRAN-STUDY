! Bu program klavyeden okunan iki sayýnýn toplamýný bulup ekrana görüntüler.

INTEGER a
INTEGER b
INTEGER sonuc

PRINT *, 'Lütfen birinci sayýyý giriniz : '
READ *, a  ! Klavyeden okuma yapan komut.

PRINT *, 'Lütfen ikinci sayýyý giriniz : '
READ *, b

sonuc = a + b   ! Bu komut assignment (atama) komutudur.

PRINT *, 'Sayýlarýn toplamý = ' , sonuc

END
